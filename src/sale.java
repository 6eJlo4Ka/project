public class sale {




    private int month;
    private int sale;

    public sale(int month, int sale) {
        this.month = month;
        this.sale = sale;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getSale() {
        return sale;
    }

    public void setSale(int sale) {
        this.sale = sale;
    }
}




